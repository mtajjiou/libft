#include "libft.h"

int		ft_count_whitespaces(char *c)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (c[j] != '\0')
	{
		if (c[j] == '\t' || c[j] == '\v' || c[j] == '\f'
			|| c[j] == '\n' || c[j] == '\r' || c[j] == ' ')
			i++;
		j++;
	}
	return (i);
}
